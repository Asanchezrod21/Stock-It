    private void generarReporteMensual() {
        Connection conectar = null;

        try {
            conectar = (Connection) conectar();
            generarReport.generarReport((java.sql.Connection) conectar);

            JOptionPane.showMessageDialog(this, "Informe mensual generado con Ã©xito", "Informe Mensual", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar el informe mensual", "Error", JOptionPane.ERROR_MESSAGE);

        } finally {
            // Cerrar recursos
            try {
                if (conectar != null) {
                    conectar.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }
